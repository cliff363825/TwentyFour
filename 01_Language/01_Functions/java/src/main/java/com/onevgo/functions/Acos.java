package com.onevgo.functions;

public class Acos {
    public static void main(String[] args) {
        System.out.println(Math.acos(0));
        System.out.println(Math.acos(1.0 / 2));
        System.out.println(Math.acos(Math.sqrt(2) / 2));
        System.out.println(Math.acos(Math.sqrt(3) / 2));
    }
}
